package com.project;

public class User {
    private String electricID;
    private String fullname;
    private float due;

    public User(String electricID, String fullname, float due) {
        this.electricID = electricID;
        this.fullname = fullname;
        this.due = due;
    }

    public float getDue() {
        return due;
    }

    public String getElectricID() {
        return electricID;
    }

    public String getFullname() {
        return fullname;
    }
}